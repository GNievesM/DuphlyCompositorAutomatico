    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (DuphlyMusicComposer.getInstance().isHayBase() && DuphlyMusicComposer.getInstance().isHayReglas()) {            
        
        List<Pair<String, String>> listRules = DuphlyMusicComposer.getInstance().getMelodyRules();

        List<Note> listNotes = DuphlyMusicComposer.getInstance().ApplyImprovisationRuleListByName(listRules, DuphlyMusicComposer.getInstance().getInternalBase(), null);
        DuphlyMusicComposer.getInstance().setListaNotasImprovisacion((ArrayList<Note>) listNotes);

        Score musicComposition = DuphlyMusicComposer.getInstance().ConvertImprovisationAndBaseToJmusic(listNotes, DuphlyMusicComposer.getInstance().getInternalBase(), 60);
       
        DuphlyMusicComposer.getInstance().setMelody(musicComposition);

        this.setVisible(false);
        Improvisador imp = new Improvisador();
        imp.setVisible(true);
          }
          else{ JOptionPane.showMessageDialog(null, "falto la base o las reglas");}
    }//GEN-LAST:event_jButton4ActionPerformed

