    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        JFileChooser jf = new JFileChooser();
        jf.showOpenDialog(null);
        ArchivoLectura a = new ArchivoLectura(jf.getSelectedFile().getAbsolutePath());
        ArrayList<Chord> lisChord = new ArrayList<Chord>();

        while (a.hayMasLineas()) {
            if (a.linea().equals("#")) {

                lisChord = new ArrayList<Chord>();
            } else {
                String lin = a.linea();
                String[] ChordArray = lin.split("@");
                Chord chord = new Chord();

                chord.SetDuration(Integer.parseInt(ChordArray[0]));
                chord.SetGrade(Integer.parseInt(ChordArray[1]));
                if (ChordArray[2].equals("false")) {
                    chord.SetSeptima(false);
                } else {
                    chord.SetSeptima(false);
                }
                lisChord.add(chord);

            }
        }
        DuphlyMusicComposer.getInstance().setInternalBase(lisChord);

        JOptionPane.showMessageDialog(null, "se cargo exitosamente");
        DuphlyMusicComposer.getInstance().setHayBase(true);
    }//GEN-LAST:event_jButton2ActionPerformed

